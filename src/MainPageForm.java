    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (clickcheck == false){
            JOptionPane.showMessageDialog(null,"Please click edit button to update profile");
        }else{
            String fname = txtFirstnm.getText();
            String lname = txtLastnm.getText();
            String Answer = txtAnsw.getText();
            String pw = txtPassw.getText();
            String cnpw = txtConfirmPassw.getText();
            String uid = txtUserID.getText();
            Integer security = CboxSQ.getSelectedIndex();
            if (pw.equals(cnpw)){
                ArrayList<String> newinfo = new ArrayList<>();
                newinfo.add(fname);
                newinfo.add(lname);
                newinfo.add(pw);
                newinfo.add(security.toString());
                newinfo.add(Answer);
                ViewProfile obj = new ViewProfile(uid);
                boolean stat = obj.UpdateProfile(newinfo);
                if(stat){
                    JOptionPane.showMessageDialog(null, "Profile successfully updated.");
                    txtFirstnm.setEditable(false);
                    txtLastnm.setEditable(false);
                    txtAnsw.setEditable(false);
                    txtPassw.setEditable(false);
                    txtConfirmPassw.setEditable(false);
                    CboxSQ.setEnabled(false);
                    clickcheck = false;
                }else{
                    JOptionPane.showMessageDialog(null, "Failed to update profile. Please Try Again!");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Password doesn't match! Please Try Again");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

