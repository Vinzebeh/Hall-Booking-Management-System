    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Load issue data from the file
    issueManager.loadIssueData(); // Use the instance of customer_issue
    refreshTableData(); // Refresh table to show the latest data

    String searchTaskID = searchTxt3.getText().trim(); // Get the input Task ID from the search text field
    if (searchTaskID.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a Task ID to search.");
        return;
    }

    // Search the issue data for the matching Task ID
    List<String[]> filteredData = new ArrayList<>();

    for (String[] row : issueManager.getIssueData()) { // Use issueManager to get issue data
        if (row[0].equals(searchTaskID)) { // Task ID is at index 0
            filteredData.add(row);
        }
    }

    // Check if any results were found
    if (filteredData.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No issues found with Task ID: " + searchTaskID);
    } else {
        // Display the filtered results in the table
        DefaultTableModel model = new DefaultTableModel(new String[] {"Task ID", "Issue", "Status"}, 0);
        for (String[] row : filteredData) {
            model.addRow(new Object[] {row[0], row[2], row[6]}); // Task ID is index 0, Issue is index 2, Status is index 6
        }
        jTable1.setModel(model); // Set the filtered data model to the table
    }

    }//GEN-LAST:event_jButton2ActionPerformed

