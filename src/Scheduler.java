    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       String hallType = null;
    String hallName = null;
    String startDate = null;
    String endDate = null;
    String remarks = null;
    String newStatus = null;

    
    int selectedRow = Table_Hall2.getSelectedRow();
    if (selectedRow >= 0) {
        // Get selected hall details from the table
        hallType = (String) Table_Hall2.getValueAt(selectedRow, 0);  // Assuming hall type is in the first column
        hallName = (String) Table_Hall2.getValueAt(selectedRow, 1);  // Assuming hall name is in the second column
        
        // Get data from the text fields and combo box
        startDate = Start_date.getText();  // Get the start date from the text field
        endDate = End_date.getText();      // Get the end date from the text field
        remarks = Remarkstxt.getText();    // Get remarks from the text field
        newStatus = (String) statusComboBox.getSelectedItem();  // Get the updated status from the JComboBox

        // Validate the status selection
        if (newStatus == null || newStatus.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a valid status.");
            return;
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a row to update.");
        return;
    }

    // Create an instance of the hall_availability class
    hall_availability hallUpdater = new hall_availability();
    try {
        // Update the hall details using the hall_availability class
        hallUpdater.updateHall(hallType, hallName, startDate, endDate, remarks, newStatus);

        // Show a success message
        JOptionPane.showMessageDialog(this, "Hall status updated successfully.");

        // Refresh the table to show the updated data
        DefaultTableModel tableModel = (DefaultTableModel) Table_Hall2.getModel();
        tableModel.setRowCount(0);
        
        try (BufferedReader br = new BufferedReader(new FileReader("hall_booking.txt"))) {
        String line;
        while ((line = br.readLine()) != null) {
            // Split the line by semicolon to extract hall details
            String[] hallDetails = line.split(";");
            
            // Ensure the array has the correct number of elements to avoid index issues
            if (hallDetails.length >= 3) {
                tableModel.addRow(hallDetails); // Add each row to the table
            }
        }
    } catch (IOException e) {
        // Display error message if file reading fails
        JOptionPane.showMessageDialog(this, "Error loading the file: " + e.getMessage());
    }

        // Clear the text fields and reset the combo box
        Start_date.setText("");
        End_date.setText("");
        Remarkstxt.setText("");
        statusComboBox.setSelectedIndex(0);  // Reset the combo box to the first item (default)
    } catch (RuntimeException e) {
        // Handle any errors that occur during the update
        JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

