    private void BookingPanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BookingPanelMouseClicked
        // TODO add your handling code here:
        onClick(BookingPanel);
        leaveClick(homePanel);
        leaveClick(userPanel);

        p1.setVisible(false);
        p2.setVisible(false);
        p3.setVisible(true);
        p4.setVisible(false);
        p1_AddStaff.setVisible(false);
        p1_UpdateStaff.setVisible(false);
        p1_DeleteStaff.setVisible(false);
        p1_ViewStaff.setVisible(false);
        p1_ViewUser.setVisible(false);
        p1_RemoveUser.setVisible(false);
        
        Admin obj = new Admin();
        DefaultTableModel model = (DefaultTableModel) BookingTable.getModel();
        model.setRowCount(0);
        ArrayList<String> bookinglist = obj.ReadBookings();
        for (int i = 0; i < bookinglist.size(); i++) {
            String info = bookinglist.get(i); //get each line in array list
            String[] details = info.split(";"); //get each info by spliting ; symbol
            model.addRow(new Object[]{details[0], details[1], details[2], details[3], (details[4]+"-"+details[5]), details[6], details[7], details[8], details[9]});
        }
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        BookingTable.setRowSorter(sorter);
        sorter.setSortKeys(Collections.singletonList(new RowSorter.SortKey(1, SortOrder.ASCENDING)));
        rbtnAllType.setSelected(true);
        
    }//GEN-LAST:event_BookingPanelMouseClicked

