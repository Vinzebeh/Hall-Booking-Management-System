    private void Customer_Issue_side_panelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Customer_Issue_side_panelMouseClicked
        // TODO add your handling code here:
        onClick(Customer_Issue_side_panel);
        leaveClick(homePanel);
        
        p1.setVisible(false);
        Customer_Issue_page.setVisible(true);
        p3.setVisible(false);
        p4.setVisible(false);
        
        try {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);  // Clear existing rows in the table
        // Open the customer issue file for reading
        FileReader fr = new FileReader("customer_issue.txt");
        BufferedReader br = new BufferedReader(fr);
        String line;
        
        // Loop through each line in the file
        while ((line = br.readLine()) != null) {
            // Split the line into its parts using semicolon as the delimiter
            String[] issueData = line.split(";");
            
            // Ensure that the row has enough columns before trying to access them
            if (issueData.length >= 7) {
                // Extract Task ID (row[0]), Issue (row[2]), and Status (row[6]) and add them to the table model
                model.addRow(new Object[] {issueData[0], issueData[2], issueData[6]});
            }
        }

        // Close the file reader
        br.close();

    } catch (IOException e) {
        // Handle the exception if there's an error reading the file
        JOptionPane.showMessageDialog(null, "Error reading customer_issue.txt: " + e.getMessage());
    }
    }//GEN-LAST:event_Customer_Issue_side_panelMouseClicked

