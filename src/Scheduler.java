    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
    // Load hall data from the file
    List<String> hallData = new ArrayList<>();
    try (BufferedReader br = new BufferedReader(new FileReader("Hall_info.txt"))) {
        String line;
        while ((line = br.readLine()) != null) {
            hallData.add(line);
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
        return; // Exit if loading data fails
    }

    String hallName = searchTxt1.getText().trim(); // Get the hall name from the search field
    boolean found = false; // Variable to check if hall is found

    for (int i = 0; i < hallData.size(); i++) {
        String[] details = hallData.get(i).split(";"); // Split the data line into details
        
        // Compare the hall name from the file with the one entered by the user
        if (details.length > 1 && details[1].equalsIgnoreCase(hallName)) {
            Hall_TypeTxt1.setSelectedItem(details[0]); // Set the Hall Type JComboBox
            Hall_NameTxt1.setText(details[1]); // Set the Hall Name field
            Capacity1.setSelectedItem(details[2]); // Set the Capacity JComboBox
            Hall_InfoTxt1.setText(details[3]); // Set the Hall Info field
            found = true; // Mark as found
            break; // Exit the loop after finding the hall
        }
    }
    
    if (!found) {
        // If no matching hall is found, show a message and clear the fields
        JOptionPane.showMessageDialog(this, "Hall not found.");
        Hall_TypeTxt1.setSelectedIndex(-1); // Clear the Hall Type JComboBox
        Hall_NameTxt1.setText("");
        Capacity1.setSelectedIndex(-1); // Clear the Capacity JComboBox
        Hall_InfoTxt1.setText("");
    }
        
    }//GEN-LAST:event_searchButtonActionPerformed

