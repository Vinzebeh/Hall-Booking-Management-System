    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) ViewTable.getModel();
        String search = txtSearch.getText();
        model.setRowCount(0);
        Admin obj = new Admin();
        ArrayList<String> sc = obj.SearchAcc(search);
        if (!sc.isEmpty()) {
            for (int i = 0; i < sc.size(); i++) { //Check the validity of input
                String info = sc.get(i);
                String[] users = info.split(";");
                if(!users[4].equals("C") && !users[4].equals("A")){
                    // add the results as a single row into the table
                    model.addRow(new Object[]{users[0], users[1], users[2], users[4], users[7]}); 
                }else {
                    JOptionPane.showMessageDialog(null, "User Not Found! Please Try Again!");
                    break;
                }
            } 
        }
        
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        ViewTable.setRowSorter(sorter);
        sorter.setSortKeys(Collections.singletonList(new RowSorter.SortKey(3, SortOrder.ASCENDING))); //sort acc alphabically according to the user role
    }//GEN-LAST:event_btnSearchActionPerformed

